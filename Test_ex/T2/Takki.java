package com.company;

public class Takki {

    String vari;
    String koko;

    public Takki() {
    }

    public Takki(String vari, String koko) {
        this.vari = vari;
        this.koko = koko;
    }

    public String getVari() {
        return vari;
    }

    public void setVari(String vari) {
        this.vari = vari;
    }

    public String getKoko() {
        return koko;
    }

    public void setKoko(String koko) {
        this.koko = koko;
    }
}
